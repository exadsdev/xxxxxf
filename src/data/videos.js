/**
 * Get all videos sorted by order
 */
export function getSortedVideos() {
    return [...videos].sort((a, b) => a.order - b.order);
}

/**
 * Get video by slug
 */
export function getVideoBySlug(slug) {
    return videos.find((video) => video.slug === slug) || null;
}

/**
 * Get video URLs
 */
export function getVideoUrls(video, baseUrl = '') {
    const youtubeId = video.youtubeId;
    return {
        pageUrl: `${baseUrl}/video/${video.slug}`,
        embedUrl: `https://www.youtube.com/embed/${youtubeId}`,
        contentUrl: `https://www.youtube.com/watch?v=${youtubeId}`,
        thumbnailUrl: `https://i.ytimg.com/vi/${youtubeId}/hqdefault.jpg`,
        thumbnailMaxRes: `https://i.ytimg.com/vi/${youtubeId}/maxresdefault.jpg`,
    };
}

/**
 * Get all video URLs for sitemap
 */
export function getAllVideoUrls(baseUrl = '') {
    return videos.map((video) => ({
        url: `${baseUrl}/video/${video.slug}`,
        video,
        urls: getVideoUrls(video, baseUrl),
    }));
}
